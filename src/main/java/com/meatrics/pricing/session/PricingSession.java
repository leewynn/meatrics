package com.meatrics.pricing.session;

import java.time.LocalDateTime;

/**
 * Pricing session entity representing the pricing_sessions table
 * Tracks pricing review sessions where users can modify and save pricing data
 */
public class PricingSession {
    private Long id;
    private String sessionName;
    private LocalDateTime createdDate;
    private LocalDateTime lastModifiedDate;
    private String status;
    private String notes;

    public PricingSession() {
    }

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSessionName() {
        return sessionName;
    }

    public void setSessionName(String sessionName) {
        this.sessionName = sessionName;
    }

    public LocalDateTime getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(LocalDateTime createdDate) {
        this.createdDate = createdDate;
    }

    public LocalDateTime getLastModifiedDate() {
        return lastModifiedDate;
    }

    public void setLastModifiedDate(LocalDateTime lastModifiedDate) {
        this.lastModifiedDate = lastModifiedDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
